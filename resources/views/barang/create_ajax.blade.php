<script>
    $(document).ready(function() {
        let tableBarang = $('#table_barang').DataTable(); // Pastikan ID tabel sesuai

        $("#form-tambah-barang").validate({
            rules: {
                kategori_id: {
                    required: true
                },
                barang_kode: {
                    required: true,
                    minlength: 2,
                    maxlength: 15
                },
                barang_nama: {
                    required: true,
                    minlength: 3,
                    maxlength: 100
                },
                harga_beli: {
                    required: true,
                    number: true,
                    min: 0
                },
                harga_jual: {
                    required: true,
                    number: true,
                    min: 0
                }
            },
            submitHandler: function(form, event) {
                event.preventDefault(); // Mencegah pengiriman form default
                console.log("Form action: ", form.action); // Debug URL

                $.ajax({
                    url: form.action, // Pastikan URL sesuai
                    type: 'POST', // Harus POST
                    data: $(form).serialize(),
                    dataType: 'json',
                    contentType: 'application/x-www-form-urlencoded',
                    success: function(response) {
                        if (response.status) {
                            $('#myModal').modal('hide');
                            Swal.fire({
                                icon: 'success',
                                title: 'Berhasil',
                                text: response.message
                            });
                            $('#form-tambah-barang')[0].reset();
                            $('#table_barang').DataTable().ajax.reload();
                        } else {
                            $('.error-text').text('');
                            if (response.msgField) {
                                $.each(response.msgField, function(prefix, val) {
                                    $('#error-' + prefix).text(val[0]);
                                });
                            }
                            Swal.fire({
                                icon: 'error',
                                title: 'Terjadi Kesalahan',
                                text: response.message
                            });
                        }
                    },
                    error: function(xhr) {
                        Swal.fire({
                            icon: 'error',
                            title: 'Error!',
                            text: 'Terjadi kesalahan saat menghubungi server'
                        });
                    }
                });
                return false; // Pastikan form tidak dikirim ulang
            },
            errorElement: 'span',
            errorPlacement: function(error, element) {
                error.addClass('invalid-feedback');
                element.closest('.form-group').append(error);
            },
            highlight: function(element) {
                $(element).addClass('is-invalid');
            },
            unhighlight: function(element) {
                $(element).removeClass('is-invalid');
            }
        });
    });
</script>